// ======================= LANGUAGE TOGGLE =======================
let currentLang = 'vi';
function toggleLanguage() {
    currentLang = currentLang === 'vi' ? 'en' : 'vi';
    alert(`Ngôn ngữ đã chuyển sang: ${currentLang === 'vi' ? 'Tiếng Việt' : 'English'}`);
}


